    <h1>Sustainability</h1>

    <p>As a conference, we are not climate neutral. However, we lower our impact on the environment as much as possible, Inspired by the 8 Râ€™s (Recycle - Refuse - Reduce - Reuse - Repair - Regift - Recover - Rethink). We welcome feedback on how to improve. Our current initiatives:</p>

    <h2>Travel</h2>

    <ul>
        <li>We're a remote-first company, and use public transport where possible.</li>
        <li>We book travel for our speakers so we can offer more sustainable alternatives (such as trains) instead of flights.</li>
        <li>We carbon offsett the flights we book.</li>
        <li>We encourage our participants to come by train if possible. Amsterdam has good train connections to Brussels, London, Paris,...</li>
    </ul>

    <h2>Venue</h2>

    <ul>
        <li>We look for venues that are easily reachable by public transport.</li>
    </ul>

    <h2>Catering</h2>

    <ul>
        <li>We offer a minimum of 50% vegetarian food (and vegan is available upon request). We try to provide a very balanced buffet, but we prefer chicken over meat and we refrain from food like tuna etc.</li>
        <li>We closely estimate our food amount through a careful headcount to minimise waste. We create awareness with our attendees to not throw away food. We are looking into the options of donating leftover food.</li>
        <li>Drinks are offered in porcelain and glassware. We provide water bottles with tap water.</li>
        <li>We provide bio straws (we can offer plastic straws for those who need it for accessibility reasons).</li>
        <li>We discussed thoroughly with our caterer the use of reusable plates. However, for this year, we are not able to provide all of our food on reusable plates. Where needed, we will use 100% biodegradable disposables that compost within 90 days and therefore meet the EN13432 standard. Of course, the sustainable use of disposables depends on a good collection and recycling.</li>
    </ul>

    <h2>Recycling</h2>

    <p>During the conference we optimize waste processing and recycling. We have separate trash cans with bio compostable garbage bags, for all compostables. We also have separate paper bins.</p>

    <h2>Logistics</h2>

    <ul>
        <li>Our totebags are fair trade and made of recycled bio cotton.</li>
        <li>Our T-shirts are 100% bio cotton.</li>
        <li>We work with suppliers that print without chemical ink.</li>
        <li>We wash reuse materials such as staff shirts, lanyards, ...</li>
        <li>We order recycled paper: stickie notes, program booklets, ...</li>
    </ul>
